        <h1>CANAL NACIONAL</h1>
        <br />
        <p class="text-para">Canal Nacional es un cauce artificial a cielo abierto con una longitud aproximada de 12 km ubicado en la Ciudad de México, el cual fue decretado el 3 de mayo de 2012 como <b>Espacio Abierto Monumental del Distrito Federal</b> por su valor ambiental, histórico y sociocultural.</p>
        <p class="text-para">Dicho cauce fue construido antes de la creación de México Tenochtitlán y fungió durante la época prehispánica como una de las rutas comerciales más importantes para el traslado de productos agrícolas de la capital, ya que, comunicaba los embarcaderos de Canal de Chalco hasta lo que hoy se conoce como los mercados de la Merced y Jamaica, cuyo abastecimiento de agua provenía de los escurrimientos del Popocatépetl, el lago de Chalco y los ojos de agua de Xochimilco y Tláhuac.</p>
        <p class="text-para">Actualmente, se puede apreciar de Canal Nacional la zona sur de Anillo Periférico hasta Río Churubusco atravesando las alcaldías Coyoacán, Iztapalapa y Xochimilco, donde se encuentran presentes ecosistemas acuáticos y vegetación que son el refugió de fauna silvestre, además de retener el suelo natural, fungir como barrera contra el viento y el ruido, amortiguar los cambios de temperatura y ofrecer un lugar de esparcimiento a la comunidad. </p>
        <div class="space"></div>
        <h2>PROGRAMA DE REHABILITACIÓN</h2>
        <p class="text-goth sub-sub">Plan maestro</p>
        <br />
        <p class="text-para">En 2019 el Gobierno de la Ciudad de México a través de la Secretaría del Medio Ambiente (SEDEMA) y el Sistema de Aguas de la Ciudad de México (SACMEX) en colaboración con las alcaldías Coyoacán, Iztapalapa y Tláhuac arrancaron el <b>Plan Maestro Canal Nacional</b>, proyecto que tiene por objetivo rehabilitar una parte del sistema lacustre de la capital y recuperar espacios públicos de gran valor ambiental para beneficiar a 240 mil personas.</p>
        <p class="text-para">El plan abarca <b>8.4 km del Canal</b> conformado por 4 directrices alineadas al desarrollo sustentable: como el equipamiento urbano, infraestructura hídrica, paisaje, medio ambiente, conectividad y movilidad. En el tema ambiental se han realizado trabajados relacionados con el retiro de vegetación acuática, prevención de erosión y compactación del suelo natural y saneamiento forestal. </p>
        <p class="text-para">Los procesos de trabajo serán realizados en tres etapas: </p>
        <ul>
            <li><b>Etapa 1:</b> 2019 Río Churubusco a Calzada de la Viga</li>
            <li><b>Etapa 2:</b> 2020 Calzada de la Viga a Calle Nimes</li>
            <li><b>Etapa 3:</b> 2021 Calle Nimes a Periférico</li>
        </ul>
